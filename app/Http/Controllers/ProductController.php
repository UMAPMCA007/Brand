    {

       $data=$request->all();

       Product::create([
           'brand_id'=>$data['brand_id'],
           'Pname'=>$data['product'],
           'brand'=>$data['brand'],
           'unit_price'=>$data['price'],
           'product_code'=>$data['product_code'],
           'manu_date'=>$data['manu_date'],
           'exp_date'=>$data['exp_date']
       ]);
       return redirect('/');
    }

